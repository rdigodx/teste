frase = input("Escreva uma frase:")

frase_contadora = frase.count('a')
frase_contadore = frase.count('e')
frase_contadori = frase.count('i')
frase_contadoro = frase.count('o')
frase_contadoru = frase.count('u')

vogais = frase_contadora + frase_contadore + frase_contadori + frase_contadoro + frase_contadoru

print(vogais)