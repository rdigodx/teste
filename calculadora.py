print(f"Resultado da soma é:{calculo1}")
calculo2 = numero1 - numero2
print(f"Resultado da subtração é:{calculo2}")
calculo3 = numero1 * numero2
print(f"Resultado da multiplicação é:{calculo3}")
calculo4 = numero1 / numero2
print(f"Resultado da divisão é:{calculo4}")




